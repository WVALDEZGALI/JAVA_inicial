public class MathematicOperations {
    public static void main(String[] args) {

        double x = 2.1;
        double y = 3.5;

        System.out.println (Math.ceil (x)); // Devuelve un entero hacia arriba.
        System.out.println (Math.floor (y)); // Devuelve un entero hacia abajo.
        System.out.println (Math.pow (x,y)); // Devuelve un numero elevado a otro, primero es el numero y el segundo el exponenete.
        System.out.println (Math.max (x,y)); // Devuelve el numero mayor.
        System.out.println (Math.sqrt (y)); // Devuelve la raiz cuadrada.
        System.out.println (Math.PI * Math.pow (y,2)); // Devuelve el area de un circulo, donde "y" es el radio de circunferencia.
        System.out.println (4 * Math.PI * Math.pow (x,2)); // Devuelve el area de una esfera, donde "x" es el radio de circunferencia.
        System.out.println ((4/3) * Math.PI * Math.pow (x,3)); // Devuelve el volumen de una esfera, donde "x" es el radio de circunferencia..





    }
}
