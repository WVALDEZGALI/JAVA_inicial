public class NamingJava {

    public static void main(String[] args) {

        // Los nombres de las variables deben inciar con una letra, $ o _

        int celPhone = 56256256;
        String $countryName = "Argentina";
        String _backgroundColor = "Blue";

        // Las constantes se escriben en mayusculas y contienen "_" para separar las palabras.

        int POSITION = -6;
        int MIN_WIDTH = 900;

        // Upper Camel Case: Letra inicial en mayuscula y se utiliza para nombrar clases.
        // Lower Camel Case: Letra inicial en minuscula y se utiliza para nombrar variables y metodos.

    }

}
